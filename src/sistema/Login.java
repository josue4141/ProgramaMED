    private void userTxtMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_userTxtMousePressed
        if (userTxt.getText().equals("Ingrese su nombre de usuario")) {

            userTxt.setText("");
            userTxt.setForeground(Color.black);

        }

        if (String.valueOf(passTxt.getPassword()).isEmpty()) {
            passTxt.setText("********");
            passTxt.setBackground(Color.gray);
        }


    }//GEN-LAST:event_userTxtMousePressed

    private void passTxtMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_passTxtMousePressed
        if (String.valueOf(passTxt.getPassword()).equals("********")) {
            passTxt.setText("");
            passTxt.setForeground(Color.black);
        }
        if (userTxt.getText().isEmpty()) {
            userTxt.setText("Ingrese su nombre de usuario");
            userTxt.setForeground(Color.gray);

        }


    }//GEN-LAST:event_passTxtMousePressed

    private final String USUARIO_VALIDO = "admin";
    private final String CONTRASENA_VALIDA = "12345";

    private void loginBtnTxtMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_loginBtnTxtMouseClicked
        // javax.swing.JOptionPane.showMessageDialog(this, "Intento de login con los datos: \nUsuario: " + userTxt.getText() + "\nContraseña: " + String.valueOf(passTxt.getPassword()), "LOGIN", javax.swing.JOptionPane.INFORMATION_MESSAGE);
        String usuario = userTxt.getText();
        String contrasena = String.valueOf(passTxt.getPassword());

        if (usuario.equals(USUARIO_VALIDO) && contrasena.equals(CONTRASENA_VALIDA)) {
            javax.swing.JOptionPane.showMessageDialog(this, "¡Inicio de sesión exitoso!", "LOGIN", javax.swing.JOptionPane.INFORMATION_MESSAGE);

            AgenteReservas ventana = new AgenteReservas();
            ventana.setVisible(true);
            ventana.setLocationRelativeTo(null);
            this.dispose(); // Esto cierra el JFrame de Login

        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Usuario o contraseña incorrectos", "ERROR", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_loginBtnTxtMouseClicked

